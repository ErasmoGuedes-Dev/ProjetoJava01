package Iniciante;

public class RestoDaDivisao {
	public static void main(String[] args) {
		
		/*Módulo: Resto da divisão % */
		double carro = 9;
		double pessoas = 2;
				
		double resto = carro % pessoas;
		
		System.out.println("Sobraram exatamente: " + resto + " está quantidade de carro");
		
	}
}
