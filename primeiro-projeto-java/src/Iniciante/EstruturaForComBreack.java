package Iniciante;

public class EstruturaForComBreack {
	public static void main(String[] args) {
		
		/*Estrutura de repetição com for e break*/
		
		for (int numero = 0; numero <= 10 ; numero ++) {
			if (numero == 5) {
				System.out.println("Oba, encontrei o número 5");
				System.out.println("Estou parando de executar...");
				break;
				
			}
			
		}
		
		
	}
}
